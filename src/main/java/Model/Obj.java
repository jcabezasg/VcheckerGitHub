package Model;

public class Obj {

    String type;
    String sha;
    String url;
   
   public Obj() {};
   
   public Obj (String type, String sha,String url) {
	   
	   this.type=type;
	   this.sha=sha;
	   this.url=url;
   }
   
   public String getType() {
	   
	   return type;
	   
   }
   
   public void setType(String type) {
	   
	   this.type=type;
   }
   
  public String getSha() {
	   
	   return sha;
	   
   }
   
   public void setSha(String sha) {
	   
	   this.sha=sha;
   }
   
   
  public String getUrl() {
	   
	   return url;
	   
   }
   
   public void setUrl(String url) {
	   
	   this.url=url;
   }
   
   
   
    
    
    
    
    
    
    





}


